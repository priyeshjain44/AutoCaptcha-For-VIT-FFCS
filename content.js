function skip(start, end, y) {
  flag = 0;
  for (var i = 0; i < start.length; ++i) {
      if (y >= start[i] && y <= end[i]) {
          flag = 1;
          break;
      }
  }
  return flag;
}

var generatePixelMatrix = function(imgData) {
	var matrix=[];
	var count=0;
	var row=[];
	for (var i=0;i<imgData.data.length;i+=4)     
	{
		if(imgData.data[i+0]==255 && imgData.data[i+1]==254 && imgData.data[i+2]==223 && imgData.data[i+3]==255)
			row.push(0);
		else
			row.push(1);
		count++;
		if(count%130==0)
		{
			matrix.push(row);
			row=[];
		}
	}
	return matrix;    
};

var getCaptcha = function(imgMatrix){
  var f=0;
  pixels=[];
  values=[];
  start=[];
  end=[];
  var x, y;
  for(var l=0; l<36 ; l++){
    var mask = keys[order[l]];
    for(var x=2;x<25;++x){
      for(var y=2;y<130;++y){
        if (skip(start, end, y))
            continue;
        var match1 = 0,match2=0;
        for(var i=0,p=x;i<mask.length;i++,p++){
          for(var j=0,q=y; j<mask[0].length;j++,q++){
            try{
              if(mask[i][j] == '1' && p<25 && q<130)
                if(imgMatrix[p][q] == '1')
                  match1++;
                 else
                    break;
          }
            catch(e){
              console.log("Error Message : " + e);
              break;
            }     
          }
        }
        for(var i=0; i<mask.length;i++)
            for(var j=0;j<mask[0].length;j++)
                if(mask[i][j]=='1')
                    match2++;
        if(match1==match2){
            values[y] = order[l];
            pixels.push(y);
            start.push(y);
            end.push(y + mask[0].length);
            f+=1;
        }
      
      }
    }
    if(f==6)
      break;
  }
  var res = sort(pixels,values);
  captcha="";
  for(var i=0; i <res.length; i++){
    captcha+=res[i];
  }
  return captcha;
}

